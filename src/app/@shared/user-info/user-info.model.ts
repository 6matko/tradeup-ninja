/**
 * Authenticated user information
 *
 * @export
 * @class UserInfo
 */
export class UserInfo {
    /**
     * URL of steam photo
     *
     * @type {string}
     * @memberof UserInfo
     */
    photoUrl: string;
    /**
     * Steam user name
     *
     * @type {string}
     * @memberof UserInfo
     */
    username: string;
    /**
     * Steam ID
     *
     * @type {string}
     * @memberof UserInfo
     */
    sub: string;
}
